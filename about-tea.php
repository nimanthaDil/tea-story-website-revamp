                            <h2>GRADES OF CEYLON BLACK TEA</h2>
                            <p>Ceylon Tea as we now know comes in different varieties, which has flavors and aromas
                                unique to itself. Along with the a variety of teas, there are varieties of grades that
                                Ceylon tea has been separated into. The grading of tea does not affect the flavors and
                                aromas or the quality for that matter, it is a mere indication of the size and
                                appearance of the tea leaf that is manufactured.
                            </p>
                            <p><strong>Ceylon Tea is basically dived into two categories</strong></p>
                            <p>1) Whole leaf grades, which has been a consistent form of grading throughout the years.
                                The Orange Pekoe (OP) grades are for the whole leaf traditional black tea.</p>
                            <p>2) Broken Leaf Grades, which are commonly used among consumers these days. Terminology of
                                Grades</p>
                            <p>Fannings– These are the leftovers of the manufacturing process, in which high quality tea
                                leaves are produced such as the Orange Pekoe. This low quality tea has experienced a
                                high demand during the last century as drinking tea became a popular habit.</p>
                            <p>Flowery– this grading consists of larger leaves, that are typically plucked in their
                                second or third flush, and tips are scarcely present. These grades fetch high prices in
                                the international markets, and are expensive to produce.</p>
                        </div>
